#!/usr/bin/env python3
"""PreCompact hook — saves minimal context before compaction.

Acts as a safety net. Claude should manually write richer context,
but this guarantees something is saved even if Claude forgets.
"""

import os
import subprocess
import sys
from datetime import datetime


def get_git_info():
    """Get current git branch and recent commit subjects."""
    info = {}
    try:
        branch = subprocess.run(
            ["git", "rev-parse", "--abbrev-ref", "HEAD"],
            capture_output=True,
            text=True,
            timeout=5,
        )
        if branch.returncode == 0:
            info["branch"] = branch.stdout.strip()

        log = subprocess.run(
            ["git", "log", "--oneline", "-5", "--no-decorate"],
            capture_output=True,
            text=True,
            timeout=5,
        )
        if log.returncode == 0:
            info["recent_commits"] = log.stdout.strip()

        status = subprocess.run(
            ["git", "status", "--short"],
            capture_output=True,
            text=True,
            timeout=5,
        )
        if status.returncode == 0:
            lines = status.stdout.strip().split("\n")
            info["dirty_files"] = len([l for l in lines if l.strip()])
    except (subprocess.TimeoutExpired, FileNotFoundError, OSError):
        pass
    return info


def main():
    cwd = os.getcwd()
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M")

    # Determine where to write
    project_dir = os.environ.get("CLAUDE_PROJECT_DIR", "")
    if project_dir:
        target = os.path.join(project_dir, ".claude", "compaction-context.md")
    else:
        target = os.path.expanduser("~/.claude/compaction-context.md")

    os.makedirs(os.path.dirname(target), exist_ok=True)

    git_info = get_git_info()

    lines = [
        "# Compaction Context (auto-saved)",
        f"",
        f"**Saved at**: {timestamp}",
        f"**Working directory**: `{cwd}`",
    ]

    if git_info.get("branch"):
        lines.append(f"**Git branch**: `{git_info['branch']}`")
    if git_info.get("dirty_files"):
        lines.append(f"**Uncommitted changes**: {git_info['dirty_files']} files")

    lines.append("")
    lines.append("## After Compaction — Reload These")
    lines.append("1. `~/CLAUDE.md` — global config")
    lines.append("2. Cross-session memory MEMORY.md")
    lines.append("3. This file — `compaction-context.md`")
    lines.append("4. Project's `.claude/CLAUDE.md` if in a project directory")

    if git_info.get("recent_commits"):
        lines.append("")
        lines.append("## Recent Commits")
        lines.append("```")
        lines.append(git_info["recent_commits"])
        lines.append("```")

    lines.append("")
    lines.append("---")
    lines.append("*Auto-generated by save-compaction-context.py. Claude should add richer context above this line.*")
    lines.append("")

    with open(target, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


if __name__ == "__main__":
    main()
